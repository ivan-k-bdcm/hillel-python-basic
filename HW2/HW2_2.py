# Метод split() повертає список із значеннями, які отримуються із введеної строки,
# шляхом її розбиття по символу ",".
# Метод map() пертворює рядкові значення в цілочисельні та присвоює їх
# змінним a, b та c.
a, b, c = map(int, input("Enter a plural:\n").split(","))

average = (a + b + c) / 3
print(f'Average: {average}')
